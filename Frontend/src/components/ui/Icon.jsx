export function VGAvatar({ name, size = 34 }) {
  const init = (name || '?').split(' ').slice(-2).map(w => w[0]).join('').toUpperCase();
  const hue = (name || '').split('').reduce((a, c) => a + c.charCodeAt(0), 0) % 360;
  return (
    <div
      className="avatar-init"
      style={{
        width: size,
        height: size,
        fontSize: size * 0.4,
        background: `linear-gradient(135deg, hsl(${hue} 55% 55%), hsl(${hue + 30} 60% 45%))`,
      }}
    >
      {init}
    </div>
  );
}

export function VGBadge({ tone = 'gray', dot = true, children }) {
  return (
    <span className={`badge badge-${tone}`}>
      {dot && <span className="bdot" />}
      {children}
    </span>
  );
}
